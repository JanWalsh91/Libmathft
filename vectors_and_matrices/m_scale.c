#include "libmathft.h"

/*
** Scales a matrix by i.
*/

t_matrix	m_scale(t_matrix m, double i)
{
    int 	x;
    int		y;

    y = -1;
    while (++y < 4) //3 ? 
    {
		x = -1;
        while (++x < 4) //3 ?
            m[y][x] *= i;
    }
    return (m);
}